/* 1. Value - семантика(struct)
Коли ви створюєте копію struct, вона повністю дублює дані.
Зміни в копії не впливають на оригінал.*/

struct Point
{    
    public int X;
    public int Y;
}

Point p1 = new Point { X = 1, Y = 2 };
Point p2 = p1; // копіювання
p2.X = 10;

    Console.WriteLine(p1.X); // 1, не 10

/* 2. Reference - семантика(class)
Об'єкти передаються за посиланням.
Зміни через будь-яку змінну, що посилається на об’єкт, впливають на сам об’єкт.*/
class PointRef
{
    public int X;
    public int Y;
}

PointRef pr1 = new PointRef { X = 1, Y = 2 };
PointRef pr2 = pr1; // посилання на той самий об'єкт
pr2.X = 10;

Console.WriteLine(pr1.X); // 10

/* Типові помилки при роботі з struct
Використання методів, які намагаються змінити внутрішній стан через копію замість 
оригіналу:*/

void MovePoint(Point p)
{
    p.X += 5;
}

Point point = new Point { X = 0, Y = 0 };
MovePoint(point);
Console.WriteLine(point.X); // 0, а не 5

/*В очах розробника здається, що зміни вплинули б на оригінал.
Неочікуване створення копій при передачі в колекції, властивості або параметри функцій. 
Це часто призводить до багів, які важко відстежити.
Практичні поради
Якщо потрібна reference-семантика, використовуйте class замість struct.
Для великих struct розгляньте передавання за посиланням (ref/in/out у C#), 
щоб уникнути непотрібних копій.
Дбайте про імітацію immutable структур, щоб уникнути непорозумінь.
Документуйте семантику, особливо якщо колеги можуть очікувати reference-поведінку.
Висновок:
Баги виникають тоді, коли код розробника не враховує value-семантику struct. 
Усвідомлення різниці між value і reference допомагає уникати несподіваних поведінок
та робити код більш передбачуваним і надійним.*/