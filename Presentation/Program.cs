Тип невеликий (≤16 байт)​
Представляє одне "значення"​
Буде readonly / immutable​
Створюється дуже часто в hot path


✕  Уникати

Багато полів/великі дані​
Потрібна ідентичність сутності​
Потрібна мутабельність як частина API​
