/* 9. Проблема захисних копій (defensive copies)​ */

/*
 Defensive copy — це автоматично створена копія значимого типу (struct) компілятором або JIT-компілятором,
 щоб уникнути можливих змін оригінального об’єкта при доступі до його членів.
 
 * Якщо метод або властивість працює з struct, який передається за значенням,
   то будь-яка зміна всередині методу не вплине на оригінал
 * Але навіть якщо struct позначений як readonly, у певних сценаріях JIT все одно створює копію,
   щоб гарантувати безпечний доступ

public struct MutableLargeStruct // НЕ readonly!​
{​
    public int Value;​

    public override string ToString() => Value.ToString();​
}​


readonly MutableLargeStruct _field = new();​

Console.WriteLine(_field.ToString());​

// компілятор створює ЗАХИСНУ КОПІЮ _field перед викликом —​

// навіть якщо ToString() насправді нічого не мутує! 
   Захисні копії часто з’являються у таких випадках:

a) Readonly struct з властивостями
 
public readonly struct Point
{
    public int X { get; }
    public int Y { get; }

    public int Sum => X + Y;
}

class Program
{
    static void Main()
    {
        var p = new Point();
        Console.WriteLine(p.Sum);
    }
}

Навіть якщо Point — readonly, при виклику p.Sum JIT може створити копію p, щоб гарантувати,
що властивість не змінить стан.

b) Доступ до елементів колекцій значимих типів

var list = new List<Point>();
list.Add(new Point());

// Це створить копію елемента, а не змінить оригінал
list[0].X.ToString();
Якщо ви працюєте з елементом struct у колекції, він повертається копією, а не посиланням.

c) Передача в методи як параметр

void Print(Point p) // p — копія
{
    Console.WriteLine(p.X);
}
Тут копія створюється при виклику методу.

    В чому проблема:
- Продуктивність: Копіювання великих структур (наприклад, 100+ байт) у циклах може сильно вплинути на швидкодію.
- Неочікувана поведінка: Зміни в копії не впливають на оригінал, що може заплутати.
- Приховані витрати: Ви можете не бачити копіювання в коді, але воно відбувається на рівні IL/JIT.

Як уникати зайвих копій:

✅ Використовувати in параметри для readonly struct:

void Print(in Point p) // передача без копії
{
    Console.WriteLine(p.X);
}
✅ Працювати з полями напряму, якщо можливо, замість властивостей, які можуть створювати копії.

✅ Не робити struct занадто великими — краще < 16 байт.

✅ Використовувати ref або ref readonly для доступу до елементів колекцій:

ref readonly var pointRef = ref list[0];


/* 10. record struct — неочевидна пастка​
 
 В C# record struct — це конструкція яка поєднує значимий тип (struct) із семантикою рівності
 за значенням та зручностями record (автогенерація Equals, GetHashCode, ToString, підтримка with-копіювання).
 
1. Значимий тип → копіювання при присвоєнні
record struct — це value type, тому при присвоєнні або передачі в метод створюється копія.
Це відрізняється від record class, який є reference type.

public record struct Point(int X, int Y);

var p1 = new Point(1, 2);
var p2 = p1; // копія

p2.X = 10;

Console.WriteLine(p1.X); // 1, а не 10!

2. with створює копію, а не змінює об’єкт
Для record struct оператор with також повертає нову копію, а не змінює існуючий екземпляр.

var p1 = new Point(1, 2);
var p2 = p1 with { X = 5 };

Console.WriteLine(p1.X); // 1
Console.WriteLine(p2.X); // 5

3. Мутація всередині методів
Якщо передати record struct у метод без ref, ви працюєте з копією.

void Move(Point p) => p.X += 1;

var p = new Point(1, 2);
Move(p);

Console.WriteLine(p.X); // 1, бо метод змінював копію

Щоб змінити оригінал, треба:

void Move(ref Point p) => p.X += 1;
 
 record class 
 — це спеціальний тип класу, оптимізований для зберігання даних, з автоматично згенерованими
   методами рівності та зручною підтримкою копіювання.
 - це клас (reference type), але з додатковими можливостями для роботи з даними.
 - Автоматична рівність за значенням
   На відміну від звичайних класів, record class порівнює об’єкти за вмістом полів/властивостей,
   а не за посиланням
 - Зручне копіювання (with)
   Можна створити копію об’єкта з частковою зміною властивостей.
 - Імм'ютабельність за замовчуванням
   Якщо використовувати positional parameters у конструкторі, властивості стають init-only
   (змінюються лише під час створення)

Чим відрізняється від record struct і звичайного class
 
Тип	            Тип даних	       Порівняння за значенням	 Імм'ютабельність за замовчуванням
class	        Reference type	   ❌ (за посиланням)	     ❌
record class	Reference type	   ✅ (за значенням)	     ✅ (якщо positional)
record struct	Value type	       ✅ (за значенням)	     ✅ (якщо positional)

Коли використовувати
 * Коли потрібен DTO або модель даних, яку часто порівнюють.
 * Для імм'ютабельних об’єктів з можливістю зручного копіювання.

/* 11. Продуктивність і кеш-локальність​

Продуктивність — це швидкість і ефективність виконання коду.
Кеш-локальність — це наскільки ефективно ваш код використовує кеш-пам’ять процесора.

struct може бути швидшим за class лише в певних сценаріях, пов’язаних із кеш-локальністю та відсутністю
додаткових витрат на керування об’єктами в heap.

1. Розташування в пам’яті
struct (value type) зберігається всередині контейнера (наприклад, у масиві або як поле іншої структури) без додаткових посилань.
class (reference type) зберігається в heap, а в масиві зберігаються лише посилання на об’єкти.

struct PointStruct { public float X, Y; }
class PointClass { public float X, Y; }

var arrStruct = new PointStruct[1_000_000]; //arrStruct → усі координати лежать послідовно в пам’яті → кеш-локальність висока.
var arrClass = new PointClass[1_000_000]; //arrClass → масив посилань, кожен об’єкт розкиданий у heap → кеш-промахи частіші.

2. Кеш-локальність і CPU
Процесор читає пам’ять блоками (cache lines, зазвичай 64 байти).
Якщо дані розташовані послідовно, при одному зверненні завантажується одразу кілька елементів → менше звернень до RAM.

Виграш від struct:

При ітерації великих масивів простих структур (наприклад, struct { float X, Y, Z; }) CPU читає їх пакетами.
Для class кожен об’єкт може бути в іншому місці → більше кеш-промахів → повільніше.

3. Коли struct дійсно швидший
✅ Коли:

Структура невелика (до ~16–32 байт).
Використовується у великих масивах або в гарячих циклах.
Потрібна щільна упаковка даних для кеш-ефективності.
Немає частих копіювань (бо struct копіюється цілком).
Тиск на GC — value types на стеку не потребують збирання​

❌ Не варто:

Якщо структура велика → копіювання стає дорогим.
Якщо потрібна поліморфна поведінка (інтерфейси, наслідування).
Якщо об’єкти часто передаються між методами → клас може бути ефективніший.

/* 12. Чек-лист і реальні баги​

✅ Використовуй struct, якщо:
Тип є невеликим і простим (зазвичай ≤ 16 байт).
Семантика значення: копія об’єкта повинна бути незалежною від оригіналу.
Немає потреби в успадкуванні (структури не підтримують наслідування, лише інтерфейси).
Імм’ютабельність (поля не змінюються після створення).
Часто створюється і знищується — уникнення алокацій у heap (структури зберігаються у стеку або inline в масивах).
Приклад: Point, DateTime, Guid.

✅ Використовуй class, якщо:
Потрібна семантика посилання (кілька змінних можуть вказувати на один об’єкт).
Об’єкт великий або складний (багато полів, вкладені об’єкти).
Потрібне успадкування або поліморфізм.
Часто передається між методами і копіювання було б дорогим.
Потрібен контроль життєвого циклу (деструктори, IDisposable).
Приклад: Stream, HttpClient, List<T>.

