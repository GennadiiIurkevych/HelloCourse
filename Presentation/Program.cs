 * List<T> зберігає елементи за значенням.
 * Вираз points[0] повертає копію елемента.
 * Зміна поля у копії не впливає на елемент у списку.
 * C# забороняє таку операцію напряму, щоб уникнути "тихих" помилок.
 
Рекомендації:

- Робити структури незмінними (immutable): усі поля readonly, зміни — тільки через конструктор.
- Якщо потрібна мутабельність — використовуємо класи (reference types).
- Використовуємо record struct для зручного створення незмінних структур.

Як правильно змінювати struct у List<T>

1. Через тимчасову змінну

var temp = points[0]; // Отримуємо копію
temp.X = 5;           // Змінюємо копію
points[0] = temp;     // Записуємо назад у список

2. Використати class замість struct

Масив vs List<T> зі структурами:

1. Зберігання та розташування в пам’яті

Масив (T[])

* Фіксована довжина, визначається при створенні.
* Елементи зберігаються послідовно в пам’яті (contiguous memory).
* Для структур це означає, що всі значення лежать поруч, що добре для кешування CPU.

List<T>

* Динамічний розмір (автоматично збільшується при додаванні елементів).
* Усередині містить масив T[], але при розширенні створює новий масив і копіює дані.
* Може мати запас місця (Capacity > Count).

2. Продуктивність

Масив

- Швидший доступ за індексом (O(1)), без додаткових перевірок розміру.
- Менше накладних витрат, особливо для великих обсягів даних.

List<T>

- Доступ за індексом також O(1), але є додаткові перевірки.
- Додавання в кінець зазвичай O(1), але іноді O(n) при розширенні.
- Видалення/вставка в середину — O(n) через зсув елементів.

3. Особливості при роботі зі структурами

Масив

* При доступі до елемента ви працюєте безпосередньо з копією значення.
* Якщо змінюєте поле структури через індекс, це змінює елемент у масиві напряму
* array[0].X = 100; — ✅ працює!
* Індексатор масиву — IL-інструкція ldelema, пряме посилання

List<T>

* При доступі через list[i] ви отримуєте копію структури.
* Зміни в копії не вплинуть на елемент у списку, якщо не присвоїти назад
* points[0].X = 100; — ❌ CS1612
* Індексатор — звичайний метод, повертає копію

4. Гнучкість

- Масив — простий, швидкий, але розмір незмінний.
- List<T> — зручні методи (Add, Remove, Find, Sort), автоматичне керування розміром.

